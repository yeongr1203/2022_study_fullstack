<?php
// for, while 둘 다 반복문.
// for는 몇 번(횟수)반복해야 되는지 알 때, for문 사용.
// while은 언제 그만둬야 하는지를 알 때, while문 사용.
// 비슷한데, 소괄호 안에 쓰는 갯수가 차이가 남.
// for(초기화값,조건식(변경할수),증감식){ }
// while 은 for문을 풀어서 사용한 것과 비슷하고, 대신 좀 지저분해보이는 단점이 있다.

$i = 0; //초기화
while ($i < 10) //조건식
{
    print $i . "<br>"; //내용
    $i++ //증감식
}



















?>